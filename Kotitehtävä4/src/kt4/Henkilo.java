package kt4;

public class Henkilo {
	private String nimi;
	private int ika;
	
	public Henkilo() {
		this.nimi = "Pekka";
		this.ika = 50;
	}

	public String getNimi() {
		return nimi;
	}

	public void setNimi(String nimi) {
		this.nimi = nimi;
	}

	public int getIka() {
		return ika;
	}

	public void setIka(int ika) {
		this.ika = ika;
	}
	
	
}
